-- Create the database context
USE master;
GO

-- Create database if it doesn't exist
IF NOT EXISTS (SELECT name FROM sys.databases WHERE name = 'StockMarketDB')
BEGIN
    CREATE DATABASE StockMarketDB;
END
GO

USE StockMarketDB;
GO

-- Table 1: Companies
CREATE TABLE Companies (
    CompanyID INT PRIMARY KEY IDENTITY(1,1),
    TickerSymbol VARCHAR(10) UNIQUE NOT NULL,
    CompanyName VARCHAR(100) NOT NULL,
    Sector VARCHAR(50) NOT NULL,
    Industry VARCHAR(50) NOT NULL,
    MarketCap BIGINT NOT NULL,
    IPOYear INT,
    ListingExchange VARCHAR(10) NOT NULL,
    Country VARCHAR(50) NOT NULL,
    Currency VARCHAR(3) DEFAULT 'USD'
);
GO

-- Table 2: StockPrices
CREATE TABLE StockPrices (
    PriceID INT PRIMARY KEY IDENTITY(1,1),
    CompanyID INT NOT NULL,
    Date DATE NOT NULL,
    OpenPrice DECIMAL(10,2) NOT NULL,
    HighPrice DECIMAL(10,2) NOT NULL,
    LowPrice DECIMAL(10,2) NOT NULL,
    ClosePrice DECIMAL(10,2) NOT NULL,
    Volume BIGINT NOT NULL,
    AdjustedClose DECIMAL(10,2) NOT NULL,
    ChangePercent DECIMAL(5,2) NOT NULL,
    FOREIGN KEY (CompanyID) REFERENCES Companies(CompanyID),
    CONSTRAINT UC_CompanyDate UNIQUE (CompanyID, Date)
);
GO

-- Create indexes for performance
CREATE INDEX IX_StockPrices_Date ON StockPrices(Date);
CREATE INDEX IX_StockPrices_CompanyID ON StockPrices(CompanyID);
CREATE INDEX IX_Companies_Sector ON Companies(Sector);
GO

